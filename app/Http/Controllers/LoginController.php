    public function login(Request $request)
    {
        //$this->create();
        $credentials = $request->only('email', 'password');

        if (!Auth::attempt($credentials)){
            return response([
                "message" => "Usuario no autorizado, revise email or pass."
            ], 403);
        } 
        
        $token = Auth::user()->createToken('passportJuan')->accessToken;
        return response()->json([
            'token' => $token,
            'user'  => auth()->user(),
        ], 200);

    }//login

